)


--TABLA RESTAURANTES-----------------------------------
CREATE TABLE RESTAURANTES(
	Id VARCHAR2(100) PRIMARY KEY,
	Direccion VARCHAR2(100),
    Municipio VARCHAR2(100),
    Zona INT,
    Telefono INT,

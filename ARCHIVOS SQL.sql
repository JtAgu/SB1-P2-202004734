--TABLA CLIENTES-----------------------------------
CREATE TABLE CLIENTES(
    DPI NUMBER PRIMARY KEY,
    Nombre VARCHAR2(100),
    Apellidos VARCHAR2(100),
    Fecha_Nacimiento DATE,
    Correo VARCHAR2(100),
    Telefono INT,
